package classes.calculator;

public class DortIslem {
  public double topla(double num1, double num2) {
    return num1 + num2;
  }

  public double cikarma(double num1, double num2) {
    return num1 - num2;
  }

  public double carpma(double num1, double num2) {
    return num1 * num2;
  }

  public double bolme(double num1, double num2) {
    return num1 / num2;
  }
}
